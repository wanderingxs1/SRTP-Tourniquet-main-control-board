uint16_t Get_Press(void)
{
	uint16_t adc_Value=0;//adc
	uint16_t adc_Press=0;//气压
	
	ADC_ChannelConfTypeDef sConfig = {0};
	sConfig.Channel = ADC_CHANNEL_1;//选择通道为channel 1
	sConfig.Rank = ADC_RANK_CHANNEL_NUMBER;
  sConfig.SamplingTime = ADC_SAMPLETIME_1CYCLE_5;
  if (HAL_ADC_ConfigChannel(&hadc, &sConfig) != HAL_OK)
    {
        Error_Handler();
    }
	//开启ADC
	HAL_ADC_Start(&hadc);
	if(HAL_OK==HAL_ADC_PollForConversion(&hadc,10))//等待ADC转换完成，超时为10ms
	{
		HAL_ADC_Stop(&hadc);
		adc_Value=HAL_ADC_GetValue(&hadc);
	}
	//转换为气压/kPa
	if(adc_Value < (int)4096*0.5/3.3)//采集到电压小于0.2V,返回为0kPa
		return 0;
	else if(adc_Value > 4096)//采集到电压大于3.3V返回为100kPa
		return 100;
	else
	{
		adc_Press = (int)((adc_Value*3.3*25/4096) - 12.5);	
		return adc_Press;
	}
}

